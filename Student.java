package ch12_151304064_151304094;

public class Student {
	private String  学号;
	private String  姓名;
	private String  性别;
	private String  出生日期;
	private String  专业;
	public String get学号(){
		return 学号;
	}
	public void set学号(String 学号){
		this.学号=学号;
	}
	public String get姓名(){
		return 姓名;
	}
	public void set姓名(String 姓名){
		this.姓名=姓名;
	}
	public String get性别(){
		return 性别;
	}
	public void set性别(String 性别){
		this.性别=性别;
	}
	public String get出生日期(){
		return 出生日期;
	}
	public void set出生日期(String 出生日期){
		this.出生日期=出生日期;
	}
	public String get专业(){
		return 专业;
	}
	public void set专业(String 专业){
		this.专业=专业;
	}
}
